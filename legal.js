  var body = document.body;
  var page = body.getAttribute("data-legal-page");
  var article = document.querySelector("article.legal-document");
  var originalArticle = article ? article.innerHTML : "";
  var originalTitle = document.title;
  var back = document.querySelector(".back");
  var footer = document.querySelector("footer .wrap");
  var originalBack = back ? back.textContent : "";
  var originalFooter = footer ? footer.innerHTML : "";

  function esc(value) {
    return String(value).replace(/[&<>\"]/g, function (char) {
      return { "&": "&amp;", "<": "&lt;", ">": "&gt;", "\"": "&quot;" }[char];
    });
  }

  function addressHtml() {
    return "<address><span dir=\"ltr\">" + esc(COMPANY) + "<br>The Meydan Hotel, Grandstand, 6th Floor<br>Meydan Road, Nad Al Sheba<br>Dubai, United Arab Emirates</span><br>البريد الإلكتروني: <a class=\"inline\" dir=\"ltr\" href=\"mailto:" + EMAIL + "\">" + EMAIL + "</a></address>";
  }

  function sectionHtml(section, index) {
    var html = "<h2><span class=\"n\">" + (index + 1) + ".</span>" + esc(section.h) + "</h2>";
    (section.p || []).forEach(function (paragraph) { html += "<p>" + esc(paragraph) + "</p>"; });
    if (section.bullets) {
      html += "<ul>" + section.bullets.map(function (item) { return "<li>" + esc(item) + "</li>"; }).join("") + "</ul>";
    }
    (section.after || []).forEach(function (paragraph) { html += "<p>" + esc(paragraph) + "</p>"; });
    if (section.address) html += addressHtml();
    return html;
  }

  function arabicArticle(data) {
    var html = "<div class=\"eyebrow\">قانوني</div><h1>" + esc(data.title) + "</h1>";
    if (data.meta) html += "<div class=\"company-meta\" dir=\"ltr\">" + data.meta + "</div>";
    if (data.sections) data.sections.forEach(function (section, index) { html += sectionHtml(section, index); });
    if (data.paragraphs) data.paragraphs.forEach(function (paragraph, index) { html += "<p" + (index === 0 ? " class=\"lead\"" : "") + ">" + esc(paragraph) + "</p>"; });
    return html;
  }

  function setLanguage(requested, persist) {
    var lang = requested === "ar" ? "ar" : "en";
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    document.querySelectorAll(".langseg").forEach(function (segment) { segment.setAttribute("data-lang", lang); });
    if (persist) {
      try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
    }

    if (lang === "ar" && ARABIC[page]) {
      article.innerHTML = arabicArticle(ARABIC[page]);
      document.title = ARABIC[page].title + " — " + COMPANY;
      if (back) back.textContent = "العودة إلى الرئيسية →";
      if (footer) footer.innerHTML = "<span>© 2026 " + COMPANY + ". جميع الحقوق محفوظة.</span><span><a href=\"privacy.html\">سياسة الخصوصية</a><a href=\"terms.html\">شروط الاستخدام</a><a href=\"disclaimer.html\">إخلاء المسؤولية</a></span>";
    } else {
      article.innerHTML = originalArticle;
      document.title = originalTitle;
      if (back) back.textContent = originalBack;
      if (footer) footer.innerHTML = originalFooter;
    }

    var current = footer && footer.querySelector('a[href="' + page + '.html"]');
    if (current) current.setAttribute("aria-current", "page");
  }

  document.querySelectorAll("[data-set-lang]").forEach(function (button) {
    button.addEventListener("click", function () { setLanguage(button.getAttribute("data-set-lang"), true); });
  });

