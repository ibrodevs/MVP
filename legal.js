  var originalBack = back ? back.textContent : "";
  var originalFooter = footer ? footer.innerHTML : "";

  function esc(value) {
    return String(value).replace(/[&<>\"]/g, function (char) {
      return { "&": "&amp;", "<": "&lt;", ">": "&gt;", "\"": "&quot;" }[char];
    });
  }

  function addressHtml(lang) {
    var emailLabel = lang === "ar" ? "البريد الإلكتروني" : "Эл. почта";
    return "<address><span dir=\"ltr\">" + esc(COMPANY) + "<br>The Meydan Hotel, Grandstand, 6th Floor<br>Meydan Road, Nad Al Sheba<br>Dubai, United Arab Emirates</span><br>" + emailLabel + ": <a class=\"inline\" dir=\"ltr\" href=\"mailto:" + EMAIL + "\">" + EMAIL + "</a></address>";
  }

  function sectionHtml(section, index, lang) {
    var html = "<h2><span class=\"n\">" + (index + 1) + ".</span>" + esc(section.h) + "</h2>";
    (section.p || []).forEach(function (paragraph) { html += "<p>" + esc(paragraph) + "</p>"; });
    if (section.bullets) {
      html += "<ul>" + section.bullets.map(function (item) { return "<li>" + esc(item) + "</li>"; }).join("") + "</ul>";
    }
    (section.after || []).forEach(function (paragraph) { html += "<p>" + esc(paragraph) + "</p>"; });
    if (section.address) html += addressHtml(lang);
    return html;
  }

  function localizedArticle(data, lang) {
    var eyebrow = lang === "ar" ? "قانوني" : "Юридическая информация";
    var html = "<div class=\"eyebrow\">" + eyebrow + "</div><h1>" + esc(data.title) + "</h1>";
    if (data.meta) html += "<div class=\"company-meta\" dir=\"ltr\">" + data.meta + "</div>";
    if (data.sections) data.sections.forEach(function (section, index) { html += sectionHtml(section, index, lang); });
    if (data.paragraphs) data.paragraphs.forEach(function (paragraph, index) { html += "<p" + (index === 0 ? " class=\"lead\"" : "") + ">" + esc(paragraph) + "</p>"; });
    return html;
  }

  function setLanguage(requested, persist) {
    var lang = requested === "ar" || requested === "ru" ? requested : "en";
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    document.querySelectorAll(".langseg").forEach(function (segment) { segment.setAttribute("data-lang", lang); });
    if (persist) {
      try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
    }

    var localized = lang === "ar" ? ARABIC : lang === "ru" ? RUSSIAN : null;
    if (localized && localized[page]) {
      article.innerHTML = localizedArticle(localized[page], lang);
      document.title = localized[page].title + " — " + COMPANY;
      if (back) back.textContent = lang === "ar" ? "العودة إلى الرئيسية →" : "← На главную";
      if (footer && lang === "ar") footer.innerHTML = "<span>© 2026 " + COMPANY + ". جميع الحقوق محفوظة.</span><span><a href=\"privacy.html\">سياسة الخصوصية</a><a href=\"terms.html\">شروط الاستخدام</a><a href=\"disclaimer.html\">إخلاء المسؤولية</a></span>";
      if (footer && lang === "ru") footer.innerHTML = "<span>© 2026 " + COMPANY + ". Все права защищены.</span><span><a href=\"privacy.html\">Политика конфиденциальности</a><a href=\"terms.html\">Условия использования</a><a href=\"disclaimer.html\">Отказ от ответственности</a></span>";
    } else {
      article.innerHTML = originalArticle;
      document.title = originalTitle;
      if (back) back.textContent = originalBack;
      if (footer) footer.innerHTML = originalFooter;
    }

    var current = footer && footer.querySelector('a[href="' + page + '.html"]');
    if (current) current.setAttribute("aria-current", "page");
  }

